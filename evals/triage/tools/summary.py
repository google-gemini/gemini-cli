import os
import json
from typing import Dict, List, Any, Optional

# Climbs up 3 levels from evals/triage/tools/ to reach the project root
PROJECT_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", "..", ".."))

def save_run_summary(run_summary: Dict[str, Any], run_dir: str) -> None:
    """Saves structured suite summary evaluation result to run_dir/summary.json."""
    with open(os.path.join(run_dir, "summary.json"), "w", encoding="utf-8") as f:
        json.dump(run_summary, f, indent=2)

def write_markdown_summary(run_summary: Dict[str, Any], filepath: str) -> None:
    """Writes a formatted markdown summary report to filepath for GHA Step Summary."""
    total = run_summary.get("total_tested", 0)
    q_match = run_summary.get("quality_categorization_rate", 0) * 100
    e_match = run_summary.get("effort_categorization_rate", 0) * 100
    ac_recall = run_summary.get("autoclose_recall_rate", 0) * 100
    ac_correct = run_summary.get("correct_autoclose_count", 0)
    ac_expected = run_summary.get("expected_autoclose_count", 0)
    spec_count = run_summary.get("workable_spec_count", 0)
    spec_pass = run_summary.get("avg_workable_spec_pass_rate_pct", 0)
    exec_time = run_summary.get("avg_execution_time_seconds", 0)
    note = run_summary.get("note", "")

    md = []
    md.append("### 📊 Triage Evaluation Summary")
    if note:
        md.append(f"**Run Note:** {note}\n")
    
    md.append("| Metric | Result | Score |")
    md.append("| :--- | :--- | :--- |")
    md.append(f"| **Quality Categorization Match** | {int(total * q_match / 100)}/{total} | **{q_match:.1f}%** |")
    if ac_expected > 0:
        md.append(f"| **Auto-Close Match (Recall)** | {ac_correct}/{ac_expected} | **{ac_recall:.1f}%** |")
    md.append(f"| **Effort Categorization Match** | {int(total * e_match / 100)}/{total} | **{e_match:.1f}%** |")
    if spec_count > 0:
        md.append(f"| **Workable Spec Pass Rate** | {spec_count} specs evaluated | **{spec_pass:.1f}%** |")
    md.append(f"| **Avg Execution Time** | - | **{exec_time:.2f}s** |")
    
    md.append("\n---")
    md.append("📥 **Detailed Logs:** Scroll down to the **Artifacts** section at the bottom of this page to download the `eval-results` ZIP containing detailed JSON files and debug logs.")
    md.append("\n*Generated by Triage Eval Runner.*")
    
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, "w", encoding="utf-8") as f:
        f.write("\n".join(md))

def calculate_and_print_summary(
    results: List[Dict[str, Any]],
    note: Optional[str],
    start_timestamp: str,
    end_timestamp: str,
    run_dir: Optional[str] = None
) -> Dict[str, Any]:
    """Calculates evaluation metrics from results list, prints summary report, and saves it if run_dir is provided."""
    total_tested = len(results)
    total_quality_matches = sum(1 for r in results if r.get("cat_eval", {}).get("quality_match"))
    total_effort_matches = sum(1 for r in results if r.get("cat_eval", {}).get("effort_match"))
    
    total_expected_autoclose = 0
    correct_autoclose = 0
    for r in results:
        cat_eval = r.get("cat_eval", {})
        exp_quality = cat_eval.get("expected_quality")
        pred_quality = cat_eval.get("predicted_quality")
        if exp_quality in ["SPAM", "EMPTY", "FEATURE"]:
            total_expected_autoclose += 1
            if pred_quality in ["SPAM", "EMPTY", "FEATURE"]:
                correct_autoclose += 1

    spec_pass_rates = [
        r.get("spec_grade", {}).get("spec_score_pct")
        for r in results
        if r.get("spec_grade") and "spec_score_pct" in r.get("spec_grade", {})
    ]
    execution_times = [r.get("execution_time_seconds", 0.0) for r in results]

    avg_spec_pass_rate = round(sum(spec_pass_rates) / len(spec_pass_rates), 1) if spec_pass_rates else 0.0
    avg_exec_time = round(sum(execution_times) / len(execution_times), 2) if execution_times else 0.0

    run_summary = {
        "start_timestamp": start_timestamp,
        "end_timestamp": end_timestamp,
        "note": note or "",
        "total_tested": total_tested,
        "workable_spec_count": len(spec_pass_rates),
        "quality_categorization_rate": total_quality_matches / total_tested if total_tested else 0,
        "effort_categorization_rate": total_effort_matches / total_tested if total_tested else 0,
        "expected_autoclose_count": total_expected_autoclose,
        "correct_autoclose_count": correct_autoclose,
        "autoclose_recall_rate": correct_autoclose / total_expected_autoclose if total_expected_autoclose else 0,
        "avg_workable_spec_pass_rate_pct": avg_spec_pass_rate,
        "avg_execution_time_seconds": avg_exec_time
    }

    print(f"\n========================================================")
    print(f"  EVALUATION SUMMARY REPORT")
    print(f"========================================================")
    print(f"  Total Tested Issues:          {total_tested}")
    if note:
        print(f"  Run Note:                     {note}")
    if total_tested > 0:
        print(f"  Quality Categorization Match: {total_quality_matches}/{total_tested} ({total_quality_matches/total_tested*100:.1f}%)")
        if total_expected_autoclose > 0:
            autoclose_rate = (correct_autoclose / total_expected_autoclose * 100.0)
            print(f"  Auto-Close Match (Recall):    {correct_autoclose}/{total_expected_autoclose} ({autoclose_rate:.1f}%)")
        print(f"  Effort Categorization Match:  {total_effort_matches}/{total_tested} ({total_effort_matches/total_tested*100:.1f}%)")
    if spec_pass_rates:
        print(f"  Workable Spec Count:          {len(spec_pass_rates)} (OK quality issues)")
        print(f"  Avg Spec Checklist Pass Rate: {avg_spec_pass_rate:.1f}%")
    print(f"  Avg Execution Time:           {avg_exec_time:.2f}s")
    print(f"========================================================")

    if run_dir:
        save_run_summary(run_summary, run_dir)
        print(f"📁 Saved structured run results to: {run_dir}/\n")
    else:
        print("ℹ️ [--no-save] Skipped persisting run results to disk.\n")

    # Write markdown summary for GHA Step Summary
    results_base = os.path.join(PROJECT_ROOT, "evals", "triage", "results")
    md_filepath = os.path.join(results_base, "latest_summary.md")
    write_markdown_summary(run_summary, md_filepath)

    return run_summary
